package com.te.springcore.assignment.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Primary;

import com.te.springcore.assignment.beans.Lion;

public class configLion {
	@Bean
	@Primary
	public Lion getLionObj() {
		Lion lion = new Lion();
		lion.creatureName();
		lion.creatureType();
		return lion;
	}

}
